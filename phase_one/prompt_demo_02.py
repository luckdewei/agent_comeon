prompt = """
判断学生的答案是否正确。

问题：我正在建造一个太阳能发电站，\
      需要帮助计算财务信息。
      土地费用 100 美元/平方英尺
      太阳能电池板费用 250 美元/平方英尺
      维护费用 100,000 美元/年 + 10 美元/平方英尺
      1 平方英尺 = 100 美元 + 250 美元 + 10 美元 = 360 美元
      总费用 = 360x + 100,000

学生的答案：设安装面积为 x 平方英尺，
      第一年总费用 = 450x + 100,000

首先，自己推导出正确答案。
然后再与学生答案对比，判断其是否正确。
在自己完成计算之前，不要先判断学生答案是否正确。
"""

response = get_completion(prompt)
